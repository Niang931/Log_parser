"""End-to-end test of the evaluation runner using DemoTiny."""
from __future__ import annotations

import csv
from pathlib import Path

from deepparse.evaluation.eval_runner import EvaluationRunner

ROOT = Path(__file__).resolve().parent.parent


def test_demo_eval_produces_perfect_metrics(monkeypatch, tmp_path):
    # Run the evaluation in an isolated artifacts directory so the test
    # never collides with developer state and we get a clean run from
    # scratch (no cached masks).
    work = tmp_path
    (work / "artifacts" / "data").mkdir(parents=True)
    (work / "artifacts" / "masks").mkdir(parents=True)
    (work / "artifacts" / "outputs" / "logs").mkdir(parents=True)
    (work / "configs").mkdir()

    (work / "configs" / "default.yaml").write_text(
        "\n".join(
            [
                "seed: 1337",
                "k: 50",
                "mode: offline",
                "strict: false",
                f"output_dir: {work}/artifacts/outputs",
                f"mask_dir: {work}/artifacts/masks",
                f"dataset_dir: {work}/artifacts/data",
                f"log_dir: {work}/artifacts/outputs/logs",
                "device: cpu",
                "deterministic: true",
            ]
        ),
        encoding="utf-8",
    )
    (work / "configs" / "demo_small.yaml").write_text(
        "\n".join(
            [
                f"base_config: {work}/configs/default.yaml",
                "datasets:",
                "  - DemoTiny",
                f"output_csv: {work}/artifacts/outputs/demo_metrics.csv",
                f"timing_csv: {work}/artifacts/outputs/demo_timing.csv",
            ]
        ),
        encoding="utf-8",
    )

    runner = EvaluationRunner(work / "configs" / "demo_small.yaml")
    rows = runner.run()
    # Per-dataset row + MacroAvg row.
    assert len(rows) == 2

    # The bundled DemoTiny ground truth is generated by the same
    # canonical pipeline that the offline stub mirrors, so a faithful
    # implementation must hit GA = PA = 1.0.
    demo = next(r for r in rows if r["dataset"] == "DemoTiny")
    assert float(demo["GA"]) == 1.0
    assert float(demo["PA"]) == 1.0

    # CSV is written with the same numbers.
    out_csv = work / "artifacts" / "outputs" / "demo_metrics.csv"
    with out_csv.open() as fh:
        reader = list(csv.DictReader(fh))
    assert any(r["dataset"] == "MacroAvg" and float(r["GA"]) == 1.0 for r in reader)
